// 배열 = 특수한 형태의 객체(참조 타입)

// 문제 상황: 한쪽 수정 → 다른 쪽도 변경
const a = [1, 2, 3];
const b = a; // 참조가 복사/공유됨
b.push(4);
console.log(b); // [ 1, 2, 3, 4 ]
console.log(a); // [ 1, 2, 3, 4 ]

// 문제 방지: 불변성을 지키는 방식으로 업데이트
const c = [...a, 5]; // 새 배열을 생성함
console.log(c); // [ 1, 2, 3, 4, 5 ]
console.log(a); // [ 1, 2, 3, 4 ]
